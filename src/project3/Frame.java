package project3;

public class Frame {
	private int pid;
	private String type;
	private int page;
	private int size;
	
	/**
	 * @return the process
	 */
	public int getPid() {
		return pid;
	}
	/**
	 * @param process the process to set
	 */
	public void setProcess(int pid) {
		this.pid = pid;
	}
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	/**
	 * @return the page
	 */
	public int getPage() {
		return page;
	}
	/**
	 * @param page the page to set
	 */
	public void setPage(int page) {
		this.page = page;
	}
	/**
	 * @return the size
	 */
	public int getSize() {
		return size;
	}
	/**
	 * @param size the size to set
	 */
	public void setSize(int size) {
		this.size = size;
	}
	
	
}
